// find the product of a*b
function basicMultiply(a, b) {
    return a * b;
  }

  //find the percentage that a is of b
  function percentage(a, b){
    return ((a/b)*100) + "%";
  }

  //find the cube of a number
  function cube(a){
    return a**3;
  }

  //find the cuberoot of a number
  function cuberoot(a){
    return a**(1/3)
  }